package aula15;

public class Condicional {

    void main() {

        // Variável
        double nota = 8;

        // Condicional
        if (nota >= 7) {
            System.out.println("Aprovado");
        } else if (nota >=5) {
            System.out.println("Recuperação");
         } else {
            System.out.println("Reprovado");
        }
    }
}
