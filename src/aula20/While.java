package aula20;

public class While {
    void main() {

        // Variável de índice
        int indice = 1;

        // Laço de repetição (exibir de 1 a 10)
        while (indice <= 10) {
            System.out.println(indice);
            indice++; // O índice sempre será incrementado 1 número a cada repetição.
        }
    }
}
